     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $request->all();
        $role = new Role();
        $role->description = $data['description'];
        $role->role = $data['role'];
        $role->status = $data['status'];
        $role->save();

        return redirect()->route('setting.index');
    }

